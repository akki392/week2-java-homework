/*Write a program to convert the upper case to lower case.*/
public class Question9 {
    public static void main(String[] args) {
        String output ="OUTPUT:";
        String name = "AKASH";
        String surname = "DALWADI";
        String language = "JAVA IS MY FAVOURITE LANGUAGE";
        String aOutputToLowerCase=output.toLowerCase();
        String aNameToLowerCase=name.toLowerCase();
        String aSurnameToLowerCase=surname.toLowerCase();
        String aLanguageToLowerCase=language.toLowerCase();
        System.out.println(aOutputToLowerCase);
        System.out.println(aNameToLowerCase);
        System.out.println(aSurnameToLowerCase);
        System.out.println(aLanguageToLowerCase);
    }
}
